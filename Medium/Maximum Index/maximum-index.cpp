class Solution{
    public:
        
    // A[]: input array
    // N: size of array
    // Function to find the maximum index difference.
    int maxIndexDiff(int a[], int n) 
    { 
        // Your code here
        int i = 0;
        int j = n - 1;
        int maxi = INT_MIN;
        while (i <= j) {
            if (a[i] > a[j]) {
                j--;
            } else {
                maxi = max(maxi, j - i);
                j = n - 1;
                i++;
            }
        }
        return maxi;
    }
};

