class Solution {
  public:
    int minSteps(int d) {
        // code here
        int sum = 0;
        int i = 1;
        while (1) {
            sum += i;
            if (sum >= d && (sum - d) % 2 == 0) {
                return i;
            }
            i++;
        }
    }
};

